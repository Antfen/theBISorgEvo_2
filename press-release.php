            <h3>Breakthrough inventions to be unveiled and Head of State landmark innovation announcement</h3>

            <p>The British Invention &amp; Technology Show, now in its 14th year is the UK’s largest expo to propel futuristic innovations and inventions into the real world of business, the global market place. This year’s event will take place at Barbican Exhibition Centre, London, from 22nd to 25th October 2014 (times on our website). Astounding Inventions &amp; Innovations will be on Show from across the Globe – Event Open for Media pre-view: 4pm - 7pm Wednesday 22nd October 2014</p>

            <p><b>Announcement for radical scale up of Global Invention at the bis Awards Gala dinner</b></p>

            <p>This will take place at the final of the coveted World Invention Awards on Friday 24th October to be held at The Savoy hotel, London with an audience of inventors, luminaries and dignitaries (names tbc). During the event, a milestone Head of State announcement, involving Britain, will be made to accelerate global invention to an unprecedented global scale. British Inventors Society (bis) founder, Kane Kramer will also outline his vision to foster exceptional creativity worldwide, providing the benchmark on the international stage for innovation and invention.</p>

            <p><b>Press Release with announcement details will be available on 24th October together with downloadable tv footage.</b></p>

            <p>Kane says ‘Global creative genius not only needs to be highlighted but given a big push in order to overcome the many odds of getting products to market and this announcement will raise the bar for global invention.’</p>

            <p><b>About bis, Invention &amp; Technology Show &amp; Awards &amp; bis Youth Sponsorship Programme</b></p>

            <p>bis, show and awards were founded by Kane Kramer, inventor of the digital audio player. The show platform connects innovators and inventors to enterprise; blue chip and multi-national companies including investors as well as designers, manufacturers and associated services.</p>

            <p>The bis Advisory Board members include, The Intellectual Patent Office, Boots Centre for Innovation, P &amp; G and Welsh Government. The work of this year’s programme is being scaled up thanks to its Royal Patron H.H. Nasser Bin Hamad Bin Isa Al Khalifa.</p>

            <p>World Invention &amp; British Invention of the Year Awards are judged by a panel of 25 expert judges, comprised of leading inventors and innovators, academics and entrepreneurs, who prioritise social and ecological benefits to mankind, originality, ingenuity as well as design in its judging criteria. Judges include representation from BAE Systems, B&amp;Q, BlackBerry &amp; the British Council.</p>

            <p>bis are proud sponsors of the Gold, Silver and Bronze medals which are awarded to both Adults &amp; Young Innovators in the under 18's &amp; 15's Classes. Accepted winners are provided with a showcase presentation area with a ticket to the British Invention of the Year Awards Dinner.</p>

            <p><b>Press Information</b> you would need to register to obtain full invention and inventor lists and the exhibitors press releases, and at http://www.bissociety.com for pictures and video news available on the night of the 24th October. url: http://www.bissociety.com Username and Password are available from our media office.</p>

            <p>For British Invention Show media registration go to, www.britishinventionshow.com/ , on the top bar click ‘Log In | Register’, under the ‘Register here’ heading, tick the ‘Media - Access to Green Menu’ (in green) for access. Please note, due to inventors secrecy of their new inventions prior to launch, most are under embargo and will not appear in our on-line listing or in press releases until the final weeks run up to the event. </p>

            <p>Please post these Show dates in your on-line diaries and notification outlets: bis 22nd-25th October Barbican London.</p>

